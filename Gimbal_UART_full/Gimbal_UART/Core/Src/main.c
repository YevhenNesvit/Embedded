/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{

  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_I2C1_Init();
  MX_TIM1_Init();
  MX_USART2_UART_Init();
  MX_USART1_UART_Init();
  /* USER CODE BEGIN 2 */
  HAL_TIM_PWM_Start(&htim1, TIM_CHANNEL_1);

  // 2. Ініціалізація Екрану
  SSD1306_Init();
  SSD1306_Fill(Black); 
  SSD1306_SetCursor(10, 10);
  SSD1306_WriteString("GIMBAL INIT...", White);
  SSD1306_UpdateScreen();
  HAL_Delay(1000); 

  // 3. Ініціалізація MPU6050 (Вихід зі сну)
  uint8_t check_data = 0;
  // Якщо датчик не відповідає - зависаємо і пишемо помилку
  if (HAL_I2C_Mem_Write(&hi2c1, MPU6050_ADDR, 0x6B, 1, &check_data, 1, 1000) != HAL_OK)
  {
      SSD1306_Fill(Black);
      SSD1306_SetCursor(0, 0);
      SSD1306_WriteString("MPU ERROR!", White);
      SSD1306_UpdateScreen();
      while(1); 
  }
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  float Kp = 3.0;   // Базова реакція
  float Ki = 0.5;  // Повільне, але точне вирівнювання (вже не 0.2!)
  // float Kd = 0.0;    // Гальмування різких рухів

  // Змінні пам'яті
  static float integral = 0;
  static float Filtered_Angle = 0;

  while (1)
  {
    if (HAL_I2C_Mem_Read(&hi2c1, MPU6050_ADDR, 0x3B, 1, rec_data, 2, 100) == HAL_OK)
    {
        // 1. Зчитуємо сирі дані
        Accel_X_RAW = (int16_t)(rec_data[0] << 8 | rec_data[1]);

        // 2. ПЕРЕТВОРЮЄМО В ГРАДУСИ ОДРАЗУ ТУТ!
        // Ділимо на 16384 і множимо на 90. 
        // Захист: якщо значення трохи вийде за 1g через шум, обмежуємо його.
        float current_g = (float)Accel_X_RAW / 16384.0f;
        if (current_g > 1.0f) current_g = 1.0f;
        if (current_g < -1.0f) current_g = -1.0f;
        float angle_degrees = asinf(current_g) * 57.2957f; // 180 / PI

        // 2. Швидкий фільтр (реакція майже миттєва)
        Filtered_Angle = (Filtered_Angle * 0.7f) + (angle_degrees * 0.3f);

        // 3. Розрахунок помилки
        float error = 0.0f - Filtered_Angle; // Ми хочемо тримати 0 градусів (горизонт)

        // 4. Інтеграл із захистом від "намотування" (Anti-Windup)
        integral += error;
        if (integral > 200.0f) integral = 200.0f;   // Обмеження!
        if (integral < -200.0f) integral = -200.0f; // Обмеження!

        // 2. PID - Інтеграл
        integral = integral + error;
        
        // Ліміт інтегралу (щоб не "надувався" надто сильно)
        if (integral > 1500) integral = 1500;
        if (integral < -1500) integral = -1500;

        // 3. Формула (Без D)
        float pid_output = (error * Kp) + (integral * Ki);

        // 4. МЕРТВА ЗОНА (Обов'язкова для дешевих серво)
        // Якщо зміна менша за 4 одиниці - ігноруємо, стоїмо на місці.
        if (fabsf(pid_output) < 4.0f) pid_output = 0;

        // 5. Вихід на мотор
        int servo_pos = 1500 + (int)pid_output;

        // Ліміти
        if (servo_pos > 2200) servo_pos = 2200;
        if (servo_pos < 800)  servo_pos = 800;

        __HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_1, servo_pos);

        // 6. Екран
        static uint8_t frame_count = 0;
        if (frame_count++ > 10) { 
            SSD1306_Fill(Black);
            SSD1306_SetCursor(20, 0); 
            SSD1306_WriteString("UART MODE", White);

            sprintf(display_buf, "Angle: %d", (int)Filtered_Angle);
            SSD1306_SetCursor(0, 20); 
            SSD1306_WriteString(display_buf, White);
            
            SSD1306_UpdateScreen();

            char uart_buf[32]; // Буфер для тексту
            
            // Формуємо повідомлення: "ANG:145\n"
            // \n (новий рядок) дуже важливий, щоб ESP32 зрозуміла кінець команди
            sprintf(uart_buf, "ANG:%d\n", (int)Filtered_Angle);
            
            // Відправляємо!
            // &huart1 - це наш порт
            // strlen(uart_buf) - довжина тексту
            // 10 - таймаут (чекаємо максимум 10 мс)
            HAL_UART_Transmit(&huart1, (uint8_t*)uart_buf, strlen(uart_buf), 10);

            frame_count = 0;
        }
    }
    HAL_Delay(20);
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
  }
  /* USER CODE END 3 */
}

